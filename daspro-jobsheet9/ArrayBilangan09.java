public class ArrayBilangan09 {  
   public static void main(String args []) {
    
        //Deklarasi array bertipe integer dengan nama bil berkapasitas 4 elemen.
        // inisialisasi elemen array sekaligus dengan menambahkan kurung kurawal.
        int[] bil = {5, 13, -7, 17};

        //Menampilkan isi elemen array ke layar.
       for (int i = 0; i <= 4; i++) {
        System.out.println(bil[i]);
       }
    }
}