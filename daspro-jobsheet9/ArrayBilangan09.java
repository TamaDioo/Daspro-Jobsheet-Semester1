public class ArrayBilangan09 {  
   public static void main(String args []) {
    
        //Deklarasi array bertipe integer dengan nama bil berkapasitas 4 elemen.
        int[] bil = new int[4];
        //Mengisi elemen array bil dengan angka 5, 13, -7, 17.
        bil[0] = 5;
        bil[1] = 13;
        bil[2] = -7;
        bil[3] = 17;

        //Menampilkan isi elemen array ke layar.
        System.out.println(bil[0]);
        System.out.println(bil[1]);
        System.out.println(bil[2]);
        System.out.println(bil[3]);
    }
}